# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam
# o número recebido como parâmetro.

def valorVezQuantidade(valor, quantidade):
    return valor * quantidade
def duplica(valor):
    return valor * 2
def triplica(valor):
    return valor * 3
def quadruplica(valor):
    return valor * 4

print(duplica(2) , " = " , valorVezQuantidade(2,2))
print(triplica(2) , " = " , valorVezQuantidade(2,3))
print(quadruplica(2) , " = " , valorVezQuantidade(2,4))
